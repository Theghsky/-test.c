/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
int* exchange(int* nums, int numsSize, int* returnSize)
{
    int low = 0, hight = numsSize - 1, tmp = 0;
    while (low < hight)
    {
        if (nums[low] % 2 == 0 && nums[hight] % 2 == 1)
        {
            tmp = nums[low];
            nums[low] = nums[hight];
            nums[hight] = tmp;
        }
        if (nums[low] % 2 == 1)
            low++;
        if (nums[hight] % 2 == 0)
            hight--;
    }
    *returnSize = numsSize;
    return nums;
}