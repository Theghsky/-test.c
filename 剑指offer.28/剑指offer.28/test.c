bool validateStackSequences(int* pushed, int pushedSize, int* popped, int poppedSize)
{
    int* satck = (int*)malloc(sizeof(int) * pushedSize);
    int top = -1, index = 0;
    for (int i = 0; i < pushedSize; i++)
    {
        satck[++top] = pushed[i];
        while ((top != -1) && satck[top] == popped[index])
        {
            top--;
            index++;
        }
    }
    if (top == -1)
        return true;
    return false;
}